package com.example.test03;

public class listDataFormat {
    private String mHole;
    private int mPar;
    private int mScore;

    public listDataFormat(String mHole, int mPar, int mScore) {
        this.mHole = mHole;
        this.mPar = mPar;
        this.mScore = mScore;
    }

    public String getmHole() {
        return mHole;
    }

    public int getmPar() {
        return mPar;
    }

    public int getmScore() {
        return mScore;
    }
}
